// Constructor function
function Con(premsg) {
  let prefix = premsg;

  // Getter function to access private member, prefix
  this.getPreMsg = function () {
    return prefix;
  };
}

// Adding out method to the PROTOTYPE of con Constructor
// These functions prototypes are declared outside of the Function Constructor,
// therefore they don't have access to the private members. That is why the
// 'getter' function, 'getPreMsg' was added.
// src:
//   https://stackoverflow.com/questions/436120/accessing-private-member-variables-from-prototype-defined-functions
Con.prototype.out = function (msg) {
  let preMsg = this.getPreMsg();
  console.log(`${preMsg} ${msg}`);
};

Con.prototype.tab = function (msg, extra = "<empty>") {
  let preMsg = this.getPreMsg();
  console.log(`${preMsg} ${extra} Start of table print >>>>>`);
  console.table(msg);
  // console.log(`${preMsg} ${extra} End of table print   >>>>>`);
};

Con.prototype.dir = function (msg, extra = "<empty>") {
  let preMsg = this.getPreMsg();
  console.log(`${preMsg} ${extra} Start of object print >>>>`);
  console.dir(msg);
  // console.log(`${preMsg} ${extra} End of object print   >>>>`);
};

/**
 * Decided to rewrite Book class. This time it should have the following:
 * 1. a private field (member).
 * 2. a counter to keep track of the number of books. Books created. A static member
 * The book uuid will be generated by the class member itself.
 **/
class Book {
  #uuid;
  #title;
  #author;
  #pages;
  #status;
  constructor(title, author, pages, status) {
    this.#uuid = this.get_new_uuid();
    this.#title = title;
    this.#author = author;
    this.#pages = pages;
    this.#status = status;
    Book.incrementBookCount();
  }
  // Field and methods to keep count of how many books were processed
  static bookCount = 0;

  static incrementBookCount = () => {
    Book.bookCount += 1;
  };

  static get_book_count() {
    return Book.bookCount;
  }

  get_new_uuid() {
    return self.crypto.randomUUID();
  }

  get_uuid() {
    return this.#uuid;
  }

  get_title() {
    return this.#title;
  }

  get_author() {
    return this.#author;
  }

  get_pages() {
    return this.#pages;
  }

  get_status() {
    return this.#status;
  }

  get info() {
    return (
      "uuid: " +
      this.uuid() +
      ", title: " +
      this.title() +
      ", author: " +
      this.author() +
      ", pages: " +
      this.pages() +
      ", status: " +
      this.status()
    );
  }

  set status(stat) {
    this.#status = stat;
  }
}

