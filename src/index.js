let form = document.getElementById("addBookForm");
form.addEventListener("submit", function (e) {
  /*
   * e.preventDefault will prevent the form from submitting.
   * If no errors detected then the form will submit(post action), if not
   * then e.preventDefault will stop the submit process.
   * The original condition for this project was to stop the form from performing
   * 'submit' process. The process is handled here in this function.
   */
  e.preventDefault();
  let bookTitle = document.getElementById("booktitle");
  let bookAuthor = document.getElementById("bookauthor");
  let bookPages = document.getElementById("bookpages");

  const cl = new Con("[form submit]");

  // NOTE: The design for the error message section:
  //         1. Create a space for the error message output
  //         2. Identify all the elements that will contribute to the error messages
  //         3. Establish the conditions that will generate error messages
  //         4. Determine when the code will proceed after checking for input errors
  let tmpErrorSection = document.getElementById("errorMsgSection");
  let errorStr = "";
  let validator = validation();

  if (tmpErrorSection.hasChildNodes()) {
    console.log("Error messages detected");
    removeAllChildElemById(tmpErrorSection);
    tmpErrorSection.classList.remove("errors_detected");
  }

  errorStr = validator.text_input(bookTitle, "book title");
  let bookTitleError =
    errorStr == "Input is valid"
      ? ""
      : createElement("p", ["error", "title"], "titleError", errorStr);
  errorStr = validator.text_input(bookAuthor, "book author");
  let bookAuthorError =
    errorStr == "Input is valid"
      ? ""
      : createElement("p", ["error", "author"], "authorError", errorStr);
  errorStr = validator.text_input(bookPages, "book pages");
  let bookPagesError =
    errorStr == "Input is valid"
      ? ""
      : createElement("p", ["error", "pages"], "pagesError", errorStr);
  // NOTE: The form will not reset and the error messages and tokens will remain on
  //       screen until the user rectifies their input
  if (
    bookTitleError !== "" ||
    bookAuthorError !== "" ||
    bookPagesError !== ""
  ) {
    console.log(
      "[form submit] Error detected, please correct before continuing...",
    );
    // Display the errors here
    let tmpErrorSectionHeading = createElement(
      "p",
      ["error", "heading"],
      "errorSectionHeading",
      "Errors detected, please rectify the inputs",
    );
    // cl.dir(bookPagesError);
    tmpErrorSection.appendChild(tmpErrorSectionHeading);
    if (bookTitleError !== "") {
      tmpErrorSection.appendChild(bookTitleError);
    }
    if (bookAuthorError !== "") {
      tmpErrorSection.appendChild(bookAuthorError);
    }
    if (bookPagesError !== "") {
      tmpErrorSection.appendChild(bookPagesError);
    }
    tmpErrorSection.classList.add("errors_detected");

    return;
  }

  const data = new FormData(form);
  let tmpTitle;
  let tmpAuthor;
  let tmpPages;
  let tmpStat;
  for (const [name, value] of data) {
    if (name == "title") {
      tmpTitle = value;
    }
    if (name == "author") {
      tmpAuthor = value;
    }
    if (name == "pages") {
      tmpPages = value;
    }
    if (name == "checkbox") {
      tmpStat = value;
    }
  }
  let tmpBook = new Book(tmpTitle, tmpAuthor, tmpPages, tmpStat);

  addBookToLibrary(tmpBook);
  displayInfo();
  form.reset();
  let para = document.getElementById("bookStat");
  para.textContent = "Unread";
});

